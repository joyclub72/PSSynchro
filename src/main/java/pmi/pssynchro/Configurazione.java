    private void SalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvaActionPerformed
        Config.setProperty("URL", jTextFieldUrl.getText());
        Config.setProperty("GIACENZA", jTextFieldGiac.getText());
        Config.setProperty("CLIENTI", jTextFieldCli.getText());
        Config.setProperty("ARTICOLI", jTextFieldArt.getText());
        Config.setProperty("FORNITORI", jTextFieldFor.getText());
        Config.setProperty("IMMAGINI", jTextFieldImm.getText());
        Config.setProperty("ALLEGATI", jTextFieldAll.getText());
        Config.setProperty("SCADENZARIO", jTextFieldSca.getText());
        Config.setProperty("STATISTICHE", jTextFieldStat.getText());
        Config.setProperty("MARCHE", jTextFieldMar.getText());
        Config.setProperty("ORDINI", jTextFieldOrd.getText());
        Config.setProperty("LINGUE", jTextFieldLin.getText());
        Config.setProperty("DATABASE", jTextFieldDb.getText());
        Config.setProperty("SERVER", jTextFieldSer.getText());
        Config.setProperty("USERNAME", jTextFieldUser.getText());
        Config.setProperty("PASSWORD", jTextFieldPwd.getText());
        Config.setProperty("PULIZIAAREA", jTextFieldClean.getText());
        Config.setProperty("URLMAGAZZINO", jTextFieldUrlMag.getText());
        Config.setProperty("CODICEMAGAZZINO", jTextFieldCodMag.getText());
        Config.setProperty("MAGAZZINO", jTextFieldMag.getText());
        Config.setProperty("LISTINI", jTextFieldLis.getText());
        Config.setProperty("CODICI_RIFERIMENTO", jTextFieldCodRif.getText());
        Config.setProperty("FTP", jTextFieldFtp.getText());
        Config.setProperty("USERNAME_FTP", jTextFieldUsernameFtp.getText());
        Config.setProperty("PASSWORD_FTP", jTextFieldPwdFtp.getText());
        Config.setProperty("SCAMBIO_FTP", jTextFieldCartellaFtp.getText());
        Config.setProperty("URL_CHECK_IMMAGINI", jTextFieldUrlCheckImg.getText());
        Config.setProperty("PERCORSO_IMMAGINE_FTP", jTextFieldPercorsoImgFtp.getText());
        Config.setProperty("IMMAGINE_FTP", jTextFieldNomeImgFtp.getText());
        String catPs = new String();
        if (chkCatPs.isSelected()) {
            catPs = "1";
        } else {
            catPs = "0";
        }
        Config.setProperty("CATEGORIEPS", catPs);
        
        String stream = new String();
        if (chkStream.isSelected()) {
            stream = "1";
        } else {
            stream = "0";
        }
        Config.setProperty("MOSTRASTREAM", stream); 
        
        String ftpPlain = new String();
        if (chkFtpPlain.isSelected()) {
            ftpPlain = "1";
        } else {
            ftpPlain = "0";
        }
        Config.setProperty("FTPPLAIN", ftpPlain);        
        
    }//GEN-LAST:event_SalvaActionPerformed

